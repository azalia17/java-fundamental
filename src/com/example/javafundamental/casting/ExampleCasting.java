package com.example.javafundamental.casting;

public class ExampleCasting {
    public static void main(String[] args){
        int a = (int) 3.14;
        System.out.println(a);

        double b = 3.14;
        String hasil = String.valueOf(b);
        System.out.println(hasil);
    }
}

/*
3 -> int
3.14 -> String
 */